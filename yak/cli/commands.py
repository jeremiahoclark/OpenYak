@app.command()
def onboard():
    """Initialize yak configuration and workspace."""
    from yak.config.loader import get_config_path, save_config
    from yak.config.schema import Config
    from yak.utils.helpers import get_workspace_path
    
    config_path = get_config_path()
    
    if config_path.exists():
        console.print(f"[yellow]Config already exists at {config_path}[/yellow]")
        if not typer.confirm("Overwrite?"):
            raise typer.Exit()
    
    # Create default config
    config = Config()
    save_config(config)
    console.print(f"[green]✓[/green] Created config at {config_path}")
    
    # Create workspace
    workspace = get_workspace_path()
    console.print(f"[green]✓[/green] Created workspace at {workspace}")
    
    # Create default bootstrap files
    _create_workspace_templates(workspace)
    
    console.print(f"\n{__logo__} yak is ready!")
    console.print("\nNext steps:")
    console.print("  1. Configure Ollama in [cyan]~/.yak/config.json[/cyan]")
    console.print("     Ensure Ollama is running and model is pulled")
    console.print("  2. Chat: [cyan]yak agent -m \"Hello!\"[/cyan]")
    console.print("\n[dim]Want Telegram/WhatsApp? See: https://github.com/jeremiahoclark/OpenYak#chat-apps[/dim]")




