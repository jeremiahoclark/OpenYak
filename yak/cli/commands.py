@app.command()
def gateway(
    port: int = typer.Option(18790, "--port", "-p", help="Gateway port"),
    verbose: bool = typer.Option(False, "--verbose", "-v", help="Verbose output"),
):
    """Start the yak gateway."""
    from yak.config.loader import load_config, get_data_dir
    from yak.bus.queue import MessageBus
    from yak.agent.loop import AgentLoop
    from yak.channels.manager import ChannelManager
    from yak.session.manager import SessionManager
    from yak.cron.service import CronService
    from yak.cron.types import CronJob
    from yak.heartbeat.service import HeartbeatService
    
    if verbose:
        import logging
        logging.basicConfig(level=logging.DEBUG)
    
    console.print(f"{__logo__} Starting yak gateway on port {port}...")
    
    config = load_config()
    bus = MessageBus()
    provider = _make_provider(config)
    session_manager = SessionManager(config.workspace_path)
    
    # Create cron service first (callback set after agent creation)
    cron_store_path = get_data_dir() / "cron" / "jobs.json"
    cron = CronService(cron_store_path)
    
    # Calendar client (optional)
    calendar_client = None
    if config.tools.calendar.enabled:
        from yak.integrations.google_calendar import GoogleCalendarClient
        calendar_client = GoogleCalendarClient(
            key_file=config.tools.calendar.service_account_key_file,
            calendar_id=config.tools.calendar.calendar_id,
            timezone=config.tools.calendar.timezone,
        )

    # Create agent with cron service
    agent = AgentLoop(
        bus=bus,
        provider=provider,
        workspace=config.workspace_path,
        model=config.agents.defaults.model,
        fallback_model=config.get_ollama_fallback_model(),
        tool_failover_threshold=config.ollama.tool_failover_threshold,
        max_iterations=config.agents.defaults.max_tool_iterations,
        brave_api_key=config.tools.web.search.api_key or None,
        exec_config=config.tools.exec,
        cron_service=cron,
        restrict_to_workspace=config.tools.restrict_to_workspace,
        session_manager=session_manager,
        calendar_client=calendar_client,
    )
    
    # Set cron callback (needs agent)
    async def on_cron_job(job: CronJob) -> str | None:
        """Execute a cron job through the agent."""
        response = await agent.process_direct(
            job.payload.message,
            session_key=f"cron:{job.id}",
            channel=job.payload.channel or "cli",
            chat_id=job.payload.to or "direct",
        )
        if job.payload.deliver and job.payload.to:
            from yak.bus.events import OutboundMessage
            await bus.publish_outbound(OutboundMessage(
                channel=job.payload.channel or "cli",
                chat_id=job.payload.to,
                content=response or ""
            ))
        return response
    cron.on_job = on_cron_job

    # Auto-register calendar digest cron job
    cal_cfg = config.tools.calendar
    if cal_cfg.enabled and cal_cfg.digest_enabled and cal_cfg.digest_channel and cal_cfg.digest_to:
        from yak.cron.types import CronSchedule as _CronSchedule
        existing = [j for j in cron.list_jobs(include_disabled=True) if j.name == "calendar-digest"]
        if not existing:
            cron.add_job(
                name="calendar-digest",
                schedule=_CronSchedule(kind="cron", expr=cal_cfg.digest_cron_expr, tz=cal_cfg.timezone),
                message=(
                    "Use the calendar tool to list today's events, then compose a brief "
                    "daily digest summary and send it via the message tool."
                ),
                deliver=True,
                channel=cal_cfg.digest_channel,
                to=cal_cfg.digest_to,
            )
            console.print("[green]✓[/green] Calendar digest cron job registered")
    
    # Create heartbeat service
    async def on_heartbeat(prompt: str) -> str:
        """Execute heartbeat through the agent."""
        return await agent.process_direct(prompt, session_key="heartbeat")
    
    heartbeat = HeartbeatService(
        workspace=config.workspace_path,
        on_heartbeat=on_heartbeat,
        interval_s=30 * 60,  # 30 minutes
        enabled=True
    )
    
    # Create channel manager
    channels = ChannelManager(config, bus, session_manager=session_manager)
    
    if channels.enabled_channels:
        console.print(f"[green]✓[/green] Channels enabled: {', '.join(channels.enabled_channels)}")
    else:
        console.print("[yellow]Warning: No channels enabled[/yellow]")
    
    cron_status = cron.status()
    if cron_status["jobs"] > 0:
        console.print(f"[green]✓[/green] Cron: {cron_status['jobs']} scheduled jobs")
    
    console.print(f"[green]✓[/green] Heartbeat: every 30m")
    
    async def run():
        try:
            await cron.start()
            await heartbeat.start()
            await asyncio.gather(
                agent.run(),
                channels.start_all(),
            )
        except KeyboardInterrupt:
            console.print("\nShutting down...")
            heartbeat.stop()
            cron.stop()
            agent.stop()
            await channels.stop_all()
    
    asyncio.run(run())




