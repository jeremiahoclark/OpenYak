def _make_provider(config):
    """Create Ollama provider from config and validate connectivity."""
    from yak.providers.ollama_provider import OllamaProvider

    provider = OllamaProvider(
        api_base=config.get_ollama_base_url(),
        default_model=config.get_ollama_model(),
        timeout_seconds=config.ollama.timeout_seconds,
    )

    healthy = asyncio.run(provider.healthcheck())
    if not healthy:
        console.print("[red]Error: Ollama is not reachable.[/red]")
        console.print(f"Checked: {config.get_ollama_base_url()}")
        console.print("Start Ollama and retry.")
        raise typer.Exit(1)
    return provider


